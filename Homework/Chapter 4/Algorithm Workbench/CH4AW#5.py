def main():
    
    numerator = 1
    
    denominator = 30
    
    total = 0
    
    while numerator <= 30:
        
        total = total + (numerator/denominator)
        
        numerator += 1
        
        denominator -= 1
           
    print (total)
  
  
main()

#Calculates the total to 93.84460105853213